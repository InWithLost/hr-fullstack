/**
 * Middleware для проверки аутентификации пользователя
 * 
 * Используется для защиты страниц, требующих авторизации пользователя.
 * Применяется к страницам, которые явно указывают его в definePageMeta.
 * Проверяет наличие токена в localStorage и перенаправляет на страницу входа,
 * если пользователь не авторизован.
 */
export default defineNuxtRouteMiddleware((to, from) => {
  // БЛОК 1: Проверка окружения и токена
  // Проверяем, что код выполняется на клиенте, так как localStorage доступен только там
  if (process.client) {
    // Получаем токен из localStorage
    const token = localStorage.getItem('token')
    
    // БЛОК 2: Перенаправление неавторизованных пользователей
    // Если токена нет, перенаправляем на страницу входа с сохранением исходного URL
    if (!token) {
      return navigateTo('/login', { 
        query: { 
          // Сохраняем исходный URL для возврата после авторизации
          redirect: to.fullPath,
          // Добавляем сообщение для пользователя о необходимости авторизации
          message: 'Для доступа к этой странице необходимо войти в систему'
        } 
      })
    }
  }
})
