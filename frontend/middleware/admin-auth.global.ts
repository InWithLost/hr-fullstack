/**
 * Глобальное middleware для логирования и базовой проверки авторизации в админ-панели
 * 
 * Это middleware выполняется для ВСЕХ маршрутов (из-за .global в имени файла)
 * и предназначено только для логирования и базовой проверки авторизации
 * без блокировки доступа к страницам админки
 */
export default defineNuxtRouteMiddleware((to) => {
  // БЛОК 1: Проверка маршрута админ-панели
  // Проверяем, относится ли маршрут к админ-панели
  if (to.path.startsWith('/admin')) {
    // Логирование для отладки - выводит информацию о переходе на админ-страницу
    console.log('Переход на админ-страницу:', to.path)
    
    // БЛОК 2: Проверка специфических страниц админки
    // Если это страница создания или редактирования вакансии, проверяем токен
    if (to.path.includes('/admin/jobs/create') || to.path.includes('/admin/jobs/') && to.path.includes('/edit')) {
      // БЛОК 3: Проверка авторизации на клиенте
      // В реальном приложении здесь была бы проверка токена и прав доступа
      // Проверяем, находимся ли мы на клиенте перед использованием localStorage
      if (process.client) {
        // Получаем токен из localStorage и логируем его наличие
        const token = localStorage.getItem('token')
        console.log('Токен авторизации:', token ? 'Присутствует' : 'Отсутствует')
      }
    }
  }
})
